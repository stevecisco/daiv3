        Assert.Equal(2, count);
    }

    [Fact]
    public async Task GetModifiedDateRangeAsync_WithinRange_ReturnsEntries()
    {
        // Arrange
        var now = DateTimeOffset.UtcNow.ToUnixTimeSeconds();
        var rangeStart = now - 1000;
        var rangeEnd = now;

        var entry1 = CreateTestEntry("T1", "D1");
        entry1.UpdatedAt = now - 500; // Within range [now-1000, now]
        
        var entry2 = CreateTestEntry("T2", "D2");
        entry2.UpdatedAt = now + 500; // Outside range - after rangeEnd

        await _repository.AddAsync(entry1, CancellationToken.None);
        await _repository.AddAsync(entry2, CancellationToken.None);

        // Act
        var results = await _repository.GetModifiedDateRangeAsync(rangeStart, rangeEnd, CancellationToken.None);

        // Assert
        Assert.NotNull(results);
        Assert.Single(results); // Only entry1 is in range
        Assert.Equal("T1", results[0].Term);
    }

    [Fact]
    public async Task GetModifiedDateRangeAsync_OpenEndedRange_ReturnsAllAfterDate()
    {
        // Arrange
        var now = DateTimeOffset.UtcNow.ToUnixTimeSeconds();
        var before = now - 1000;

        var entry = CreateTestEntry("T1", "D1");
        entry.UpdatedAt = now - 500;
        await _repository.AddAsync(entry, CancellationToken.None);

        // Act
        var results = await _repository.GetModifiedDateRangeAsync(before, beforeDate: null, CancellationToken.None);

        // Assert
        Assert.NotNull(results);
        Assert.Single(results);
    }

    [Fact]
    public async Task GetAllAsync_MultipleEntries_ReturnsSortedByTerm()
    {
        // Arrange
        await _repository.AddAsync(CreateTestEntry("Zebra", "Z"), CancellationToken.None);
        await _repository.AddAsync(CreateTestEntry("Apple", "A"), CancellationToken.None);
        await _repository.AddAsync(CreateTestEntry("Banana", "B"), CancellationToken.None);

        // Act
        var all = await _repository.GetAllAsync(CancellationToken.None);

        // Assert
        Assert.NotNull(all);
        Assert.Equal(3, all.Count);
        Assert.Equal("Apple", all[0].Term);
        Assert.Equal("Banana", all[1].Term);
        Assert.Equal("Zebra", all[2].Term);
    }

    [Fact]
    public async Task RoundTrip_WithRelatedTerms_PersistsAndRetrievesJson()
    {
        // Arrange
        var entry = CreateTestEntry("Document", "A file containing knowledge.", "Knowledge");
        entry.RelatedTermsJson = "[\"Text\", \"File\", \"Knowledge Base\"]";

        // Act
        await _repository.AddAsync(entry, CancellationToken.None);
        var retrieved = await _repository.GetByIdAsync(entry.GlossaryId, CancellationToken.None);

        // Assert
        Assert.NotNull(retrieved);
        Assert.Equal("[\"Text\", \"File\", \"Knowledge Base\"]", retrieved.RelatedTermsJson);
    }

    [Fact]
    public async Task RoundTrip_WithNotes_PersistsAndRetrievesNotes()
    {
        // Arrange
        var entry = CreateTestEntry("Agent", "An autonomous system that performs tasks.");
        entry.Notes = "Introduced in Phase 5 for advanced automation features.";

        // Act
        await _repository.AddAsync(entry, CancellationToken.None);
        var retrieved = await _repository.GetByIdAsync(entry.GlossaryId, CancellationToken.None);

        // Assert
        Assert.NotNull(retrieved);
        Assert.Equal("Introduced in Phase 5 for advanced automation features.", retrieved.Notes);
    }
}
