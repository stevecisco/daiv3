    }

    [Fact]
    public async Task GetCountByCategoryAsync_ValidCategory_ReturnsCorrectCount()
    {
        // Arrange
        await _repository.AddAsync(CreateTestEntry("T1", "D1", "Cat1"), CancellationToken.None);
        await _repository.AddAsync(CreateTestEntry("T2", "D2", "Cat1"), CancellationToken.None);
        await _repository.AddAsync(CreateTestEntry("T3", "D3", "Cat2"), CancellationToken.None);

        // Act
        var count = await _repository.GetCountByCategoryAsync("Cat1", CancellationToken.None);

